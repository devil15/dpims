    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<smstest_gui.Pestremedy> merged = new ArrayList<smstest_gui.Pestremedy>(pestremedyList.size());
            for (smstest_gui.Pestremedy t : pestremedyList) {
                merged.add(entityManager.merge(t));
            }
            pestremedyList.clear();
            pestremedyList.addAll(merged);
        } 
        JOptionPane.showMessageDialog(this.getParent(),"Record Updated Successfully , PLease reload the page to view the changes");
        
    }//GEN-LAST:event_jButton1ActionPerformed

