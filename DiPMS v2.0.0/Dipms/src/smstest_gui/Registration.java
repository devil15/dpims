    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ( mno_check())
        {
            firstname=jTextField2.getText();
            lastname=jTextField3.getText();
            try{
            latitude=Double.parseDouble(jTextField4.getText());
            longitude=Double.parseDouble(jTextField5.getText());
            
            
            email=jTextField6.getText();
            state=jTextField7.getText();
            address=jTextArea1.getText();
            village=jTextField8.getText();
            block=jTextField9.getText();
            district=jTextField10.getText();
            
            Connection con_enter=dbcon.in();
            PreparedStatement ps =con_enter.prepareStatement("insert into users values(?,?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1,mobile );
            ps.setString(2, firstname);
            ps.setString(3, lastname);
            ps.setDouble(4, latitude);
            ps.setDouble(5,longitude );
            ps.setString(6, email);
            ps.setString(7, state);
            ps.setString(8, address);
            ps.setString(9, village);
            ps.setString(10, block);
            ps.setString(11, district);
            ps.executeUpdate();
            ps.close();      
            
          
            JOptionPane.showMessageDialog(this,"User data updated successfully. ");
            
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(this.getParent(),"Please enter valid geographcal coordinates,");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this.getParent(),"The entered data is not valid.The possible reason may be duplication of mobile no.");
                Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            
        }
        else 
            JOptionPane.showMessageDialog(this.getParent(),"please fill the mandatory fields");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

