    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
                                            
           Connection con_autofill=dbcon.in();
           
           String auto_fill_query="select * from users where m_no="+jTextField1.getText();
           try {
               Statement  smt_autofill=con_autofill.createStatement();
               ResultSet rs=smt_autofill.executeQuery(auto_fill_query);
               while (rs.next())
               {
                   mno=rs.getString(1);
                   jTextField2.setText(rs.getString(2));
                   jTextField3.setText(rs.getString(3));
                   jTextField4.setText(String.valueOf(rs.getDouble(4)));
                   jTextField5.setText(String.valueOf(rs.getDouble(5)));
                   jTextField6.setText(rs.getString(9));
                   jTextField7.setText(rs.getString(10));
                   jTextField8.setText(rs.getString(11));
                   jTextField9.setText(rs.getString(6));
                   jTextField10.setText(rs.getString(7));
                   jTextArea1.setText(rs.getString(8));
               }
           } catch (SQLException ex) {
               Logger.getLogger(update_user.class.getName()).log(Level.SEVERE, null, ex);
           }
           
           
      
       
        
    }//GEN-LAST:event_jTextField1FocusLost

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         
        if ( mno_check())
        {
            firstname=jTextField2.getText();
            lastname=jTextField3.getText();
            try{
            latitude=Double.parseDouble(jTextField4.getText());
            longitude=Double.parseDouble(jTextField5.getText());
            
            
            email=jTextField9.getText();
            state=jTextField10.getText();
            address=jTextArea1.getText();
            village=jTextField6.getText();
            block=jTextField7.getText();
            district=jTextField8.getText();
            
            Connection con_enter=dbcon.in();
            PreparedStatement ps =con_enter.prepareStatement
            ("update users set m_no=?,fisrt_name=?,last_name=?,latitude=?,longitude=?,email=?,state=?,address=?,village=?,block=?,district=? where m_no=?");
            ps.setString(1,mobile);
            ps.setString(2, firstname);
            ps.setString(3, lastname);
            ps.setDouble(4, latitude);
            ps.setDouble(5,longitude );
            ps.setString(6, email);
            ps.setString(7, state);
            ps.setString(8, address);
            ps.setString(9, village);
            ps.setString(10, block);
            ps.setString(11, district);
            ps.setString(12, mno);
            ps.executeUpdate();
            ps.close();      
            
          
            JOptionPane.showMessageDialog(this,"User data updated successfully. ");
            
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(this.getParent(),"Please enter valid geographcal coordinates,");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this.getParent(),"The entered data is not valid.The possible reason may be duplication of mobile no.");
                Logger.getLogger(update_user.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            
        }
        else 
            JOptionPane.showMessageDialog(this.getParent(),"please fill the mandatory fields");
    }//GEN-LAST:event_jButton1ActionPerformed

